package com.acctrue.tts.model;

public class ChargeStoreInCode {
	private String storeInId;
	private String code;
	private int isStoreIn;

	public String getStoreInId() {
		return storeInId;
	}

	public void setStoreInId(String storeInId) {
		this.storeInId = storeInId;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public int getIsStoreIn() {
		return isStoreIn;
	}

	public void setIsStoreIn(int isStoreIn) {
		this.isStoreIn = isStoreIn;
	}

	
}
